        DoomniteMetalRenderer* renderer = (__bridge DoomniteMetalRenderer*)rendererPtr;"
        [renderer updateEngine:deltaTime];"
    }"
    "
    void RenderInView(void* rendererPtr, void* mtkView) {"
        DoomniteMetalRenderer* renderer = (__bridge DoomniteMetalRenderer*)rendererPtr;"
        MTKView* view = (__bridge MTKView*)mtkView;"
