// MARK: - MTKViewDelegate"

- (void)mtkView:(MTKView*)view drawableSizeWillChange:(CGSize)size {"
    _screenWidth = size.width;"
    _screenHeight = size.height;"
    "
    // Update projection matrix"
    float aspect = size.width / size.height;"
    _projectionMatrix = Doomnite::Mat4Perspective(M_PI/3.0f, aspect, 0.1f, 1000.0f);"
}"

- (void)drawInMTKView:(MTKView*)view {"
    @autoreleasepool {"
        // Get drawable"
        id<MTLDrawable> drawable = view.currentDrawable;"
        "
        // Create command buffer"
        id<MTLCommandBuffer> commandBuffer = [_commandQueue commandBuffer];"
        "
        // Create render pass descriptor"
        MTLRenderPassDescriptor* renderPassDesc = view.currentRenderPassDescriptor;"
        if (!renderPassDesc) return;"
        "
        // Clear color"
        renderPassDesc.colorAttachments[0].clearColor = MTLClearColorMake(0.1, 0.1, 0.15, 1.0);"
        renderPassDesc.colorAttachments[0].loadAction = MTLLoadActionClear;"
        "
        // Create encoder"
        id<MTLRenderCommandEncoder> encoder = [commandBuffer renderCommandEncoderWithDescriptor:renderPassDesc];"
        "
        // Get view matrix from camera (simplified)"
        simd_float4x4 viewMatrix = Doomnite::Mat4Identity();"
        // Would get from active camera component"
        "
        // Update time"
        _time += 0.016f;"  // Assume 60 FPS"
        "
        // MARK: Render all entities with mesh/components"
        if (_engine) {"
            Doomnite::GameEngine* engine = static_cast<Doomnite::GameEngine*>(_enginePtr);"
            "
            // Get all entities with Transform + Mesh"
            auto entities = engine->getEntityManager().getComponentStorage()"
                            .getAllComponents<Doomnite::TransformComponent>();"
            "
            for (const auto& pair : entities) {"
                Doomnite::Entity entity = pair.first;"
                auto transform = pair.second;"
                "
                // Get mesh component"
                auto meshComp = engine->getEntityManager().getComponentStorage()"
                                 .getComponent<Doomnite::MeshComponent>(entity);"
                "
                if (!meshComp) continue;"
                "
                // Set view/projection matrices"
                [encoder setVertexBytes:&viewMatrix"
                                length:sizeof(simd_float4x4)"
                               atIndex:BufferIndex::FrameUniformBuffer];"
                "
                [encoder setVertexBytes:&_projectionMatrix"
                                length:sizeof(simd_float4x4)"
                               atIndex:BufferIndex::FrameUniformBuffer + 1];"
                "
                // Set model matrix"
                simd_float4x4 modelMatrix = transform->modelMatrix();"
                [encoder setVertexBytes:&modelMatrix"
                                length:sizeof(simd_float4x4)"
                               atIndex:BufferIndex::ModelUniformBuffer];"
                "
                // Draw (would use actual mesh buffers)"
                // [encoder drawPrimitives:MTLPrimitiveTypeTriangle"
                //                 vertexStart:0"
                //                 vertexCount:mesh->getVertexCount()];"
            }"
        }"
        "
        // MARK: Render particles"
        [self renderParticles:encoder];"
        "
        // MARK: Render UI (would use separate pipeline)"
        [self renderUI:encoder];"
        "
        [encoder endEncoding];"
        [commandBuffer presentDrawable:drawable];"
        [commandBuffer commit];"
    }"
}"

