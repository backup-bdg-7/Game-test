extern "C" {"
    void* CreateMetalRenderer(void* device, void* enginePtr) {"
        id<MTLDevice> mtlDevice = (__bridge id<MTLDevice>)device;"
        Doomnite::GameEngine* engine = static_cast<Doomnite::GameEngine*>(enginePtr);"
        "
        DoomniteMetalRenderer* renderer = [[DoomniteMetalRenderer alloc] initWithDevice:mtlDevice"
                                                                  engine:enginePtr];"
        return (__bridge void*)renderer;"
    }"
    "
