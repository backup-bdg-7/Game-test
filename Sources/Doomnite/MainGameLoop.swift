    }"
    "
    // MARK: - Game Control"
    "
    func startNewGame() {"
        guard let ptr = enginePtr, let state = gameState else { return }"
        gamePhase = .playing"
        isPaused = false"
        "
        // Create player"
        let playerId = CreatePlayerEntity(ptr, x: 0, y: 1, z: 0)"
        AddTransformComponent(ptr, entityId: playerId, x: 0, y: 1, z: 0, rotX: 0, rotY: 0, rotZ: 0)"
        AddHealthComponent(ptr, entityId: playerId, health: 100.0)"
        "
        // Start day/night cycle"
        SetDayNightTime(ptr, time: 18000.0) // 5:00 AM"
        "
        // Load starting zone"
        LoadZone(ptr, zoneId: "zone_starting")"
    }"
    "
    func pauseGame() {"
        isPaused = true"
        if let ptr = enginePtr {"
            PauseCompleteGameEngine(ptr)"
        }"
    }"
    "
    func resumeGame() {"
        isPaused = false"
        if let ptr = enginePtr {"
            ResumeCompleteGameEngine(ptr)"
        }"
    }"
    "
    func saveGame() {"
