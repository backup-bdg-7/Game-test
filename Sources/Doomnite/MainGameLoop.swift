class MainGameLoop: ObservableObject {"
    @Published var enginePtr: UnsafeMutableRawPointer?"
    @Published var isPaused: Bool = false"
    @Published var gamePhase: GamePhase = .mainMenu"
    "
    private var displayLink: CADisplayLink?"
    private var lastTime: CFTimeInterval = 0"
    private var frameCount: Int = 0"
    private var fps: Int = 60"
    private var gameState: GameStateManager?"
    "
    init() {"
        // 1. Create C++ Game Engine via Objective-C++ bridge"
        enginePtr = CreateCompleteGameEngine()"
        "
        // 2. Set up game state manager"
        if let ptr = enginePtr {"
            gameState = GameStateManager(enginePtr: ptr)"
        }"
        "
        // 3. Initialize all systems in correct order"
        if let ptr = enginePtr {"
            InitializeAllSystems(ptr)"
        }"
    }"
    "
    deinit {"
        if let ptr = enginePtr {"
            DestroyCompleteGameEngine(ptr)"
        }"
        enginePtr = nil"
    }"
    "
    // MARK: - Start Game Loop"
    "
    func startGameLoop(in view: MTKView) {"
        guard let ptr = enginePtr else { return }"
        "
        // Setup Metal view"
        view.device = MTLCreateSystemDefaultDevice()"
        view.isPaused = false"
        view.delegate = self"
        view.framebufferOnly = true"
        view.colorPixelFormat = .bgra8Unorm_srgb"
        view.depthStencilPixelFormat = .depth32Float"
        "
        // Start display link"
        displayLink = CADisplayLink(target: self, selector: #selector(gameLoopTick))"
        displayLink?.add(to: .main, forMode: .common)"
        lastTime = CACurrentMediaTime()"
    }"
    "
    @objc func gameLoopTick() {"
        guard let displayLink = displayLink, let ptr = enginePtr else { return }"
        "
        // Calculate delta time"
        let currentTime = CACurrentMediaTime()"
        let deltaTime = Float(currentTime - lastTime)"
        lastTime = currentTime"
        "
        // Update FPS counter"
        frameCount += 1"
        if frameCount >= 60 {"
            fps = Int(1.0 / deltaTime)"
            frameCount = 0"
        }"
        "
        // Skip if paused"
        if isPaused { return }"
        "
        // MARK: - UPDATE GAME ENGINE (C++)"
        UpdateCompleteGameEngine(ptr, deltaTime)"
        "
        // MARK: - UPDATE SWIFTUI STATE"
        updateSwiftUIState()"
        "
        // MARK: - TRIGGER METAL DRAW"
        // The MTKView delegate will handle drawing"
    }"
    "
    private func updateSwiftUIState() {"
        guard let state = gameState else { return }"
        "
