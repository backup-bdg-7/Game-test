// MARK: - SwiftUI App Entry Point"

@main"
struct DoomniteApp: App {"
    @StateObject private var gameLoop = MainGameLoop()"
    @Environment(\.scenePhase) private var scenePhase"
    "
    var body: some Scene {"
        WindowGroup {"
            ContentView(gameLoop: gameLoop)"
        }"
        .onChange(of: scenePhase) { newPhase in"
            switch newPhase {"
            case .background:"
                gameLoop.saveGame()"
            case .inactive:"
                gameLoop.pauseGame()"
            case .active:"
                gameLoop.resumeGame()"
            default:"
                break"
            }"
        }"
    }"
}"

