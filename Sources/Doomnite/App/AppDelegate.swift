@main"
struct DoomniteApp: App {"
    @UIApplicationDelegateAdapter"
    var window: UIWindow?"
    "
    func application(_ application: UIApplication,"
                     didFinishLaunchingWithOptions launchOptions: [UIApplication.LaunchOptionsKey: Any]?) -> Bool {"
        "
        // 1. Create main window"
        window = UIWindow(frame: UIScreen.main.bounds)"
        "
        // 2. Initialize C++ Game Engine via Objective-C++ bridge"
        let enginePtr = CreateGameEngine()"
        "
        // 3. Create main menu (SwiftUI)"
        let gameState = GameStateManager(enginePtr: enginePtr)"
        let contentView = MainMenuView(gameState: gameState)"
        "
        // 4. Embed in hosting controller"
        let hostingController = UIHostingController(rootView: contentView)"
        hostingController.modalPresentationStyle = .fullScreen"
        window?.rootViewController = hostingController"
        window?.makeKeyAndVisible()"
        "
        // 5. Set up notification observers"
        setupNotifications()"
        "
        return true"
    }"
    "
    func applicationWillResignActive(_ application: UIApplication) {"
        // Pause game if in-game"
        if let ptr = getEnginePointer() {"
            PauseEngine(ptr)"
        )"
    }"
    "
    func applicationDidEnterBackground(_ application: UIApplication) {"
        // Save game"
        if let ptr = getEnginePointer() {"
            SaveGame(ptr)"
        )"
    }"
    "
    func applicationWillEnterForeground(_ application: UIApplication) {"
        // Prepare to resume"
    }"
    "
    func applicationDidBecomeActive(_ application: UIApplication) {"
        // Resume game if in-game"
        if let ptr = getEnginePointer() {"
            ResumeEngine(ptr)"
        )"
    }"
    "
    func applicationWillTerminate(_ application: UIApplication) {"
        // Save and cleanup"
        if let ptr = getEnginePointer() {"
            SaveGame(ptr)"
            DestroyGameEngine(ptr)"
        )"
    }"
    "
    // MARK: - Private Methods"
    "
    private func setupNotifications() {"
        NotificationCenter.default.addObserver("
            forName: UIApplication.didReceiveMemoryWarningNotification,"
            object: nil,"
            queue: .main"
