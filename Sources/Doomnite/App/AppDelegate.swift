// MARK: - GameStateManager (Bridges C++ ↔ SwiftUI)"

class GameStateManager: ObservableObject {"
    @Published var enginePtr: UnsafeMutableRawPointer?"
    @Published var gamePhase: GamePhase = .mainMenu"
    @Published var isPaused: Bool = false"
    @Published var playerHealth: Float = 100.0"
    @Published var playerMaxHealth: Float = 100.0"
    @Published var playerMana: Float = 50.0"
    @Published var playerMaxMana: Float = 50.0"
    @Published var playerLevel: Int = 1"
    @Published var playerXP: Float = 0.0"
    @Published var xpToNextLevel: Float = 100.0"
    @Published var activeQuests: [QuestInfo] = []"
    @Published var showHints: Bool = true"
    @Published var enableTutorial: Bool = true"
    @Published var isOnlineMode: Bool = false"
    "
    init(enginePtr: UnsafeMutableRawPointer) {"
        self.enginePtr = enginePtr"
        "
        // Load saved settings"
        loadSettings()"
    }"
    "
    // MARK: - Game Control"
    "
    func startNewGame() {"
        guard let ptr = enginePtr else { return }"
        gamePhase = .playing"
        isPaused = false"
        "
        // Create player via C++"
        let playerId = CreatePlayer(ptr)"
        AddTransformComponent(ptr, entityId: playerId, posX: 0, posY: 1, posZ: 0, rotX: 0, rotY: 0, rotZ: 0)"
        AddHealthComponent(ptr, entityId: playerId, health: 100.0)"
        AddPlayerDataComponent(ptr, entityId: playerId)"
        "
        // Start day/night cycle"
        SetDayNight(ptr, time: 18000.0)  // 5:00 AM"
        "
        // Load starting zone"
        LoadZone(ptr, zoneId: "zone_starting")"
    }"
    "
    func loadGame() {"
        guard let ptr = enginePtr else { return }"
        gamePhase = .playing"
        "
        if isOnlineMode {"
            // Sync from server"
            SyncFromServer(ptr)"
        } else {"
            // Load from local file"
            LoadFromLocalFile(ptr)"
        }"
    }"
    "
    func saveGame() {"
        guard let ptr = enginePtr else { return }"
        "
        if isOnlineMode {"
            SaveToServer(ptr)"
        } else {"
            SaveToLocalFile(ptr)"
        }"
    }"
    "
    func pauseGame() {"
        guard let ptr = enginePtr, !isPaused else { return }"
        isPaused = true"
        PauseEngine(ptr)"
    }"
    "
    func resumeGame() {"
        guard let ptr = enginePtr, isPaused else { return }"
        isPaused = false"
        ResumeEngine(ptr)"
    }"
    "
    func quitToMenu() {"
        saveGame()"
        gamePhase = .mainMenu"
    }"
    "
    // MARK: - Game Loop (called from Metal view)"
    "
    func update(deltaTime: Float) {"
        guard gamePhase == .playing, !isPaused, let ptr = enginePtr else { return }"
        "
        // Update C++ engine"
        UpdateEngine(ptr, deltaTime)"
        "
        // Update SwiftUI state (poll from C++)"
        updatePlayerState()"
    }"
    "
    private func updatePlayerState() {"
        // In production: read from C++ engine"
        // playerHealth = GetPlayerHealth(enginePtr)"
    }"
    "
    // MARK: - Settings"
    "
    private func loadSettings() {"
        showHints = UserDefaults.standard.bool(forKey: "showHints") ?? true"
        enableTutorial = UserDefaults.standard.bool(forKey: "enableTutorial") ?? true"
        isOnlineMode = UserDefaults.standard.bool(forKey: "isOnlineMode") ?? false"
    }"
    "
    func saveSettings() {"
        UserDefaults.standard.set(showHints, forKey: "showHints")"
        UserDefaults.standard.set(enableTutorial, forKey: "enableTutorial")"
        UserDefaults.standard.set(isOnlineMode, forKey: "isOnlineMode")"
    }"
    "
    // MARK: - Multiplayer"
    "
    func quickMatch() {"
        guard let ptr = enginePtr else { return }"
        ConnectToServer(ptr, url: "wss://doomnite-server.com")"
    }"
    "
    func createLobby(name: String, maxPlayers: Int = 4, isPublic: Bool = true) {"
        guard let ptr = enginePtr else { return }"
        CreateLobby(ptr, lobbyId: UUID().uuidString, name: name, maxPlayers: Int32(maxPlayers), isPublic: isPublic)"
    }"
    "
    func joinLobby(code: String) {"
        guard let ptr = enginePtr else { return }"
        JoinLobby(ptr, lobbyId: code)"
    }"
}"

